  @Test
  public void parseCoronaAnamnese() throws IOException, XmlException {
    OPERATIONALTEMPLATE template =
        TemplateDocument.Factory.parse(OperationalTemplateTestData.CORONA_ANAMNESE.getStream())
            .getTemplate();

    OPTParser cut = new OPTParser(template);
    WebTemplate actual = cut.parse();
    actual = new Filter().filter(actual);
    assertThat(actual).isNotNull();

    ObjectMapper objectMapper = new ObjectMapper();
    WebTemplate expected =
        objectMapper.readValue(
            IOUtils.toString(WebTemplateTestData.CORONA.getStream(), StandardCharsets.UTF_8),
            WebTemplate.class);

    List<String> errors = compareWebTemplate(actual, expected);

    // Nodes wich are generated by the parser but are not in the example

    assertThat(errors)
        .filteredOn(s -> s.contains("Extra Node"))
        .containsExactlyInAnyOrder(
            "Extra Node id=end_time aql=/context/end_time",
            "Extra Node id=health_care_facility aql=/context/health_care_facility",
            "Extra Node id=aufenthalt_in_den_letzten_14_tage_in_einem_der_risikogebiete_f端r_coronainfektion_oder_kontakt_zu_menschen_die_dort_waren aql=/content[openEHR-EHR-SECTION.adhoc.v1 and name/value='Risikogebiet']/items[openEHR-EHR-OBSERVATION.travel_history.v0]/data[at0001]/events[at0002]/data[at0003]/items[at0111 and name/value='Aufenthalt in den letzten 14 Tage in einem der Risikogebiete f端r Coronainfektion oder Kontakt zu Menschen, die dort waren']/value");

    // Nodes wich are in the example but are not generated by the parser
    assertThat(errors)
        .filteredOn(s -> s.contains("Missing Node"))
        .containsExactlyInAnyOrder(
            "Missing Node id=action_archetype_id aql=/content[openEHR-EHR-SECTION.adhoc.v1 and name/value='Allgemeine Angaben']/items[openEHR-EHR-INSTRUCTION.service_request.v1]/activities[at0001]/action_archetype_id",
            "Missing Node id=aufenthalt_in_den_letzten_14_tage_in_einem_der_risikogebiete_f端r_coronainfektion_oder_kontakt_zu_menschen_die_dort_waren aql=/content[openEHR-EHR-SECTION.adhoc.v1 and name/value='Risikogebiet']/items[openEHR-EHR-OBSERVATION.travel_history.v0]/data[at0001]/events[at0002]/data[at0003]/items[at0111 and name/value='Aufenthalt in den letzten 14 Tage in einem der Risikogebiete f端r Coronainfektion oder Kontakt zu Menschen']/value");
  }

